<?php

namespace App\Mail;

use App\Models\CourseEnrollment;
use Illuminate\Bus\Queueable;
use Illuminate\Contracts\Queue\ShouldQueue;
use Illuminate\Mail\Mailable;
use Illuminate\Mail\Mailables\Content;
use Illuminate\Mail\Mailables\Envelope;
use Illuminate\Queue\SerializesModels;

class FreeEnrollmentConfirmedMail extends Mailable implements ShouldQueue
{
    use Queueable, SerializesModels;

    public function __construct(public readonly CourseEnrollment $enrollment) {}

    public function envelope(): Envelope
    {
        return new Envelope(
            subject: 'Enrollment confirmed â€” ' . ($this->enrollment->course?->title ?? 'Akuru Institute'),
        );
    }

    public function content(): Content
    {
        return new Content(view: 'emails.free-enrollment-confirmed');
    }
}
